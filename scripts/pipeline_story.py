#!/usr/bin/env python3
"""Generate an interactive pipeline story HTML for a strategy.

Reads pipeline artifacts (strategy.md, scores, diffs, reviews) and produces
a self-contained HTML dashboard showing the end-to-end Agentic SDLC flow.

Usage:
    python3 scripts/pipeline_story.py RHAISTRAT-1749 \\
        --data-dir /path/to/pipeline-data/RHAISTRAT-1749 \\
        --output-dir epic-reports
"""

