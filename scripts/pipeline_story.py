def read_file(path):
    """Read file contents, return empty string if missing."""
    try:
        with open(path, "r") as f:
            return f.read()
    except FileNotFoundError:
        return ""


def read_json(path):
    """Read JSON file, return empty dict if missing."""
    try:
        with open(path, "r") as f:
            return json.load(f)
    except (FileNotFoundError, json.JSONDecodeError):
        return {}


def parse_strategy_sections(md_text):
    """Parse strategy.md into named sections by h2 headers."""
    sections = {}
    current_key = "_preamble"
    current_lines = []

    for line in md_text.splitlines():
        if line.startswith("## "):
            if current_lines:
                sections[current_key] = "\n".join(current_lines).strip()
            current_key = line[3:].strip()
            current_lines = []
        else:
            current_lines.append(line)

    if current_lines:
        sections[current_key] = "\n".join(current_lines).strip()

    return sections


def parse_subsections(text):
    """Split a section body by ### headers into ordered list of (title, content)."""
    subsections = []
    current_title = None
    current_lines = []
    for line in text.splitlines():
        if line.startswith("### "):
            if current_title is not None:
                subsections.append((current_title, "\n".join(current_lines).strip()))
            current_title = line[4:].strip()
            current_lines = []
        else:
            current_lines.append(line)
    if current_title is not None:
        subsections.append((current_title, "\n".join(current_lines).strip()))
    return subsections


def extract_title(md_text):
    """Extract h1 title from markdown."""
    for line in md_text.splitlines():
        if line.startswith("# "):
            return line[2:].strip()
    return ""


def extract_tldr(sections):
    """Extract TL;DR from the Strategy section."""
    strat = sections.get("Strategy (AI Generated by Agentic SDLC Pipeline)", "")
    in_tldr = False
    tldr_lines = []
    for line in strat.splitlines():
        if line.strip().startswith("### TL;DR"):
            in_tldr = True
            continue
        if in_tldr:
            if line.startswith("###") or line.startswith("## "):
                break
            tldr_lines.append(line)
    return "\n".join(tldr_lines).strip()


def extract_tables(sections):
    """Extract markdown tables from sections as list of dicts."""
    results = {}
    for key in ["Affected Components", "Impacted Teams"]:
        strat = sections.get("Strategy (AI Generated by Agentic SDLC Pipeline)", "")
        table_lines = []
        in_section = False
        for line in strat.splitlines():
            if f"### {key}" in line:
                in_section = True
                continue
            if in_section:
                if line.startswith("###") or line.startswith("## "):
                    break
                if "|" in line:
                    table_lines.append(line)

        if len(table_lines) >= 2:
            headers = [h.strip() for h in table_lines[0].split("|") if h.strip()]
            rows = []
            for tl in table_lines[2:]:
                cells = [c.strip() for c in tl.split("|") if c.strip()]
                if cells:
                    rows.append(cells)
            results[key] = {"headers": headers, "rows": rows}

    return results


def escape(text):
    """HTML-escape text."""
    return html.escape(str(text)) if text else ""


def md_inline_to_html(text):
    """Convert basic inline markdown (bold, italic, code) to HTML."""
    if not text:
        return ""
    t = html.escape(str(text))
    t = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', t)
    t = re.sub(r'\*(.+?)\*', r'<em>\1</em>', t)
    t = re.sub(r'`(.+?)`', r'<code class="text-blue-400 bg-slate-800 px-1 rounded text-xs">\1</code>', t)
    t = t.replace("\n", "<br>")
    return t


def md_to_html(text):
    """Convert markdown with headers, lists, paragraphs, and inline formatting to HTML."""
    if not text:
        return ""
    lines = text.strip().splitlines()
    html_parts = []
    in_list = False
    for line in lines:
        stripped = line.strip()
        if not stripped:
            if in_list:
                html_parts.append("</ul>")
                in_list = False
            html_parts.append("")
            continue
        if stripped.startswith("### "):
            if in_list:
                html_parts.append("</ul>")
                in_list = False
            html_parts.append(f'<h5 class="font-semibold text-slate-200 mt-4 mb-2">{md_inline_to_html(stripped[4:])}</h5>')
        elif stripped.startswith("- "):
            if not in_list:
                html_parts.append('<ul class="list-disc list-inside space-y-1 ml-2">')
                in_list = True
            html_parts.append(f'<li>{md_inline_to_html(stripped[2:])}</li>')
        else:
            if in_list:
                html_parts.append("</ul>")
                in_list = False
            html_parts.append(f'<p class="mb-2">{md_inline_to_html(stripped)}</p>')
    if in_list:
        html_parts.append("</ul>")
    return "\n".join(html_parts)


def diff_to_html(patch_text):
    """Convert a unified diff to syntax-highlighted HTML."""
    if not patch_text:
        return '<p class="text-slate-500 italic">No diff available</p>'

    lines_html = []
    for line in patch_text.splitlines():
        escaped = escape(line)
        if line.startswith("+++") or line.startswith("---"):
            lines_html.append(
                f'<div class="text-slate-400 font-semibold">{escaped}</div>'
            )
        elif line.startswith("@@"):
            lines_html.append(
                f'<div class="text-purple-400 font-semibold bg-purple-900/20">{escaped}</div>'
            )
        elif line.startswith("+"):
            lines_html.append(
                f'<div class="text-emerald-400 bg-emerald-900/20">{escaped}</div>'
            )
        elif line.startswith("-"):
            lines_html.append(
                f'<div class="text-red-400 bg-red-900/20">{escaped}</div>'
            )
        elif line.startswith("diff "):
            lines_html.append(
                f'<div class="text-blue-400 font-bold mt-2">{escaped}</div>'
            )
        else:
            lines_html.append(f'<div class="text-slate-300">{escaped}</div>')

    return "\n".join(lines_html)


def md_table_to_html(table_data, extra_classes=""):
    """Render a parsed markdown table as HTML."""
    if not table_data:
        return ""
    headers = table_data["headers"]
    rows = table_data["rows"]

    th = "".join(
        f'<th class="px-4 py-3 text-left text-xs font-semibold text-slate-400 uppercase tracking-wider">{escape(h)}</th>'
        for h in headers
    )
    body = ""
    for row in rows:
        cells = "".join(
            f'<td class="px-4 py-3 text-sm text-slate-300 border-t border-slate-700/50">{escape(c)}</td>'
            for c in row
        )
        body += f"<tr>{cells}</tr>"

    return f"""
    <div class="overflow-x-auto {extra_classes}">
      <table class="w-full">
        <thead><tr class="border-b border-slate-700">{th}</tr></thead>
        <tbody>{body}</tbody>
      </table>
    </div>"""


def generate_pipeline_story(strat_key, data_dir, output_dir="epic-reports",
                            screenshots_dir=None):
    """Generate pipeline story HTML for a strategy."""
    os.makedirs(output_dir, exist_ok=True)

    strategy_md = read_file(os.path.join(data_dir, "strategy.md"))
    summary_json = read_json(os.path.join(data_dir, "strategy-summary.json"))
    run_log_text = read_file(os.path.join(data_dir, "run-log.jsonl"))

    run_log_entries = []
    for line in run_log_text.strip().splitlines():
        line = line.strip()
        if line:
            try:
                run_log_entries.append(json.loads(line))
            except json.JSONDecodeError:
                pass

    sections = parse_strategy_sections(strategy_md)
    title = extract_title(strategy_md)
    tldr = extract_tldr(sections)
    tables = extract_tables(sections)

    epics = summary_json.get("epics", [])
    epic_with_pr = None
    for ep in epics:
        if ep.get("pr_url"):
            epic_with_pr = ep
            break

    # Load epic titles, dependencies, and blocks
    epic_titles = {}
    epic_deps = {}
    epic_blocks = {}
    jira_epics = read_json(os.path.join(data_dir, "jira-epics.json"))
    if isinstance(jira_epics, list) and jira_epics:
        for je in jira_epics:
            eid = je["epic_id"]
            epic_titles[eid] = je.get("title", "")
            epic_deps[eid] = je.get("dependencies") or []
            epic_blocks[eid] = je.get("blocks") or []
    else:
        for ep in epics:
            eid = ep["epic_id"]
            task_md = read_file(os.path.join(data_dir, eid, "epic-task.md"))
            meta_yaml = read_file(os.path.join(data_dir, eid, "run-metadata.yaml"))
            for line in task_md.splitlines():
                if line.startswith("title:"):
                    epic_titles[eid] = line.split(":", 1)[1].strip()
            blocked_by = []
            in_blocked = False
            for line in meta_yaml.splitlines():
                if line.startswith("blocked_by:"):
                    in_blocked = True
                    continue
                if in_blocked:
                    if line.startswith("- "):
                        blocked_by.append(line.strip().lstrip("- "))
                    else:
                        in_blocked = False
            epic_deps[eid] = blocked_by
            for dep in blocked_by:
                epic_blocks.setdefault(dep, []).append(eid)

    all_epic_data = {}
    for ep in epics:
        eid = ep["epic_id"]
        epic_dir = os.path.join(data_dir, eid)
        has_artifacts = os.path.exists(os.path.join(epic_dir, "v1"))
        all_epic_data[eid] = {
            "has_artifacts": has_artifacts,
            "spec": read_file(os.path.join(epic_dir, "codegen-spec.md")),
            "plan": read_file(os.path.join(epic_dir, "codegen-plan.md")),
            "task": read_file(os.path.join(epic_dir, "epic-task.md")),
            "metadata": read_file(os.path.join(epic_dir, "run-metadata.yaml")),
            "pr_replies": read_json(os.path.join(epic_dir, "pr-replies.json")),
            "v1_scores": read_json(os.path.join(epic_dir, "v1", "scores.json")),
            "v1_diff": read_file(os.path.join(epic_dir, "v1", "diff.patch")),
            "v2_feedback": read_file(
                os.path.join(epic_dir, "v2", "review-feedback.md")
            ),
            "v2_sanity": read_file(
                os.path.join(epic_dir, "v2", "sanity-check.md")
            ),
        }

    epic_data = all_epic_data.get(
        epic_with_pr["epic_id"], {}
    ) if epic_with_pr else {}

    screenshots_base = screenshots_dir or os.path.join(output_dir, "screenshots")

    rfe_summary = sections.get("Summary", "")
    customers_raw = sections.get("Affected Customers", "")
    customers = re.sub(
        r'\*\*[A-Z][A-Za-z\s&\.\'-]+\*\*(?=\s+and other)',
        '**[Customer]**',
        customers_raw,
    )
    customers = customers.replace("wx.ai", "[partner product]")
    biz_justification = sections.get("Business Justification", "").replace("wx.ai", "[partner product]")
    acceptance = sections.get("Acceptance Criteria", "")
    staff_engineer = sections.get("Staff Engineer / SME Input", "")
    staff_engineer_tags = ""
    for line in staff_engineer.splitlines():
        if line.startswith("### "):
            tag_text = escape(line[4:].strip())
            staff_engineer_tags += f'<span class="px-2 py-1 rounded-md bg-slate-700/50 text-slate-300 text-xs">{tag_text}</span>\n'

    rfe_sections = [
        ("Summary", rfe_summary),
        ("Affected Customers", customers),
        ("Business Justification", biz_justification),
        ("Acceptance Criteria", acceptance),
    ]
    rfe_tree_html = ""
    for sec_title, sec_content in rfe_sections:
        if not sec_content.strip():
            continue
        rfe_tree_html += f"""
        <details class="group/sub mb-1">
          <summary class="cursor-pointer text-xs font-semibold text-slate-400 hover:text-slate-200 flex items-center gap-1 py-1">
            <svg class="w-3 h-3 transition-transform group-open/sub:rotate-90" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 5l7 7-7 7"/></svg>
            {escape(sec_title)}
          </summary>
          <div class="ml-4 mt-1 mb-2 text-slate-300 text-sm leading-relaxed border-l-2 border-slate-700/50 pl-3">{md_to_html(sec_content)}</div>
        </details>"""

    strat_body = sections.get("Strategy (AI Generated by Agentic SDLC Pipeline)", "")
    strat_subsections = parse_subsections(strat_body)
    strat_tree_html = ""
    for sub_title, sub_content in strat_subsections:
        if sub_title == "TL;DR":
            continue
        strat_tree_html += f"""
        <details class="group/sub mb-1">
          <summary class="cursor-pointer text-xs font-semibold text-slate-400 hover:text-slate-200 flex items-center gap-1 py-1">
            <svg class="w-3 h-3 transition-transform group-open/sub:rotate-90" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 5l7 7-7 7"/></svg>
            {escape(sub_title)}
          </summary>
          <div class="ml-4 mt-1 mb-2 text-slate-300 text-sm leading-relaxed border-l-2 border-slate-700/50 pl-3">{md_to_html(sub_content)}</div>
        </details>"""

    scores = epic_data.get("v1_scores", {})
    dims = scores.get("dimensions", {})
    weighted_avg = scores.get("weighted_average", 0)
    verdict = scores.get("verdict", "")

    pr_replies = epic_data.get("pr_replies", {}).get("replies", [])

    # Build vertical DAG HTML
    epic_status_map = {ep["epic_id"]: ep.get("status", "") for ep in epics}

    def dag_node_html(eid):
        status = epic_status_map.get(eid, "")
        short_id = eid.split("-")[-1]
        ep_title = epic_titles.get(eid, "")
        short_title = ep_title[:30] + "..." if len(ep_title) > 30 else ep_title
        if status == "PRCreated":
            node_bg = "bg-blue-900/40 border-blue-600/60"
            node_text = "text-blue-300"
            dot_color = "bg-blue-500"
            status_label = "PR Created"
        elif status in ("Done", "Closed"):
            node_bg = "bg-emerald-900/40 border-emerald-600/60"
            node_text = "text-emerald-300"
            dot_color = "bg-emerald-500"
            status_label = "Done"
        else:
            node_bg = "bg-red-900/30 border-red-700/40"
            node_text = "text-red-300"
            dot_color = "bg-red-500"
            status_label = "Blocked"
        return f"""<div class="arch-node {node_bg} border rounded-lg px-4 py-3 text-center min-w-[160px]">
            <p class="font-mono text-xs font-bold {node_text}">{escape(short_id)}</p>
            <p class="text-[10px] text-slate-400 mt-1 leading-tight">{escape(short_title)}</p>
            <div class="flex items-center justify-center gap-1.5 mt-2">
              <span class="w-1.5 h-1.5 rounded-full {dot_color}"></span>
              <span class="text-[10px] text-slate-500">{escape(status_label)}</span>
            </div>
          </div>"""

    down_arrow = """<div class="flex justify-center py-1">
        <svg class="w-5 h-5 text-slate-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 14l-7 7-7-7"/>
        </svg>
      </div>"""

    fork_connector = """<div class="flex justify-center py-1">
        <svg class="w-5 h-5 text-slate-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 14l-7 7-7-7"/>
        </svg>
      </div>"""

    # Build DAG by topological layers
    roots = [eid for eid in [ep["epic_id"] for ep in epics] if not epic_deps.get(eid)]
    visited = set()
    layers = []

    def build_layers(current_layer):
        if not current_layer:
            return
        layers.append(current_layer)
        visited.update(current_layer)
        next_layer = []
        for eid in current_layer:
            for child in epic_blocks.get(eid, []):
                if child not in visited and child in epic_status_map:
                    all_deps_visited = all(d in visited for d in epic_deps.get(child, []))
                    if all_deps_visited and child not in next_layer:
                        next_layer.append(child)
        build_layers(next_layer)

    build_layers(roots)

    dep_graph_dag = '<div class="flex flex-col items-center gap-0">'
    for li, layer in enumerate(layers):
        if li > 0:
            dep_graph_dag += fork_connector
        if len(layer) == 1:
            dep_graph_dag += dag_node_html(layer[0])
        else:
            dep_graph_dag += '<div class="flex items-start gap-6 justify-center">'
            for eid in layer:
                dep_graph_dag += dag_node_html(eid)
            dep_graph_dag += '</div>'
    dep_graph_dag += '</div>'

    # Build score bars HTML
    score_bars = ""
    for dim_name, dim_data in dims.items():
        score_val = dim_data.get("score", 0)
        weight = dim_data.get("weight", 0)
        pct = score_val * 10
        color = "bg-emerald-500" if score_val >= 8 else ("bg-amber-500" if score_val >= 6 else "bg-red-500")
        score_bars += f"""
        <div class="flex items-center gap-3 mb-2">
          <span class="w-24 text-sm font-medium text-slate-300 capitalize">{escape(dim_name)}</span>
          <span class="w-10 text-xs text-slate-500 text-right">{int(weight*100)}%</span>
          <div class="flex-1 h-5 bg-slate-700 rounded-full overflow-hidden">
            <div class="{color} h-full rounded-full transition-all duration-1000" style="width: {pct}%"></div>
          </div>
          <span class="w-12 text-sm font-bold text-slate-200 text-right">{score_val}/10</span>
        </div>"""

    epic_cards_html = ""
    for ep in epics:
        eid = ep["epic_id"]
        status = ep.get("status", "Unknown")
        repo = ep.get("target_repo", "")
        ep_title = epic_titles.get(eid, "")
        badge_color = "bg-blue-500/20 text-blue-400" if status == "PRCreated" else "bg-red-500/20 text-red-400"
        pr_link = ""
        if ep.get("pr_url"):
            pr_link = f'<a href="{escape(ep["pr_url"])}" target="_blank" class="text-blue-400 hover:text-blue-300 text-xs">View PR &rarr;</a>'

        epic_cards_html += f"""
        <div class="bg-slate-800/50 border border-slate-700/50 rounded-lg p-4">
          <div class="flex items-center justify-between mb-2">
            <a href="https://redhat.atlassian.net/browse/{escape(eid)}" target="_blank"
               class="font-mono text-sm font-bold text-blue-400 hover:text-blue-300">{escape(eid)}</a>
            <span class="px-2 py-0.5 rounded text-xs font-semibold {badge_color}">{escape(status)}</span>
          </div>
          <p class="text-sm text-slate-300 mb-1">{escape(ep_title)}</p>
          <p class="text-xs text-slate-500 font-mono mb-1">{escape(repo)}</p>
          {pr_link}
        </div>"""

    # PR conversation HTML
    pr_convo_html = ""
    if pr_replies:
        for reply in pr_replies:
            user = reply.get("user", "Unknown")
            body = reply.get("reply_body", "")
            action = reply.get("action", "")
            ts = reply.get("timestamp", "")
            pr_convo_html += f"""
            <div class="flex gap-3 mb-3">
              <div class="w-8 h-8 rounded-full bg-slate-700 flex items-center justify-center text-xs font-bold text-slate-300 shrink-0">
                {escape(user[0:2])}
              </div>
              <div class="flex-1 bg-slate-800/50 rounded-lg p-3 border border-slate-700/50">
                <div class="flex items-center gap-2 mb-1">
                  <span class="text-sm font-semibold text-slate-300">{escape(user)}</span>
                  <span class="px-1.5 py-0.5 rounded text-xs bg-amber-500/20 text-amber-400">{escape(action)}</span>
                  <span class="text-xs text-slate-500">{escape(ts)}</span>
                </div>
                <p class="text-sm text-slate-400">{escape(body)}</p>
              </div>
            </div>"""
    else:
        pr_convo_html = '<p class="text-slate-500 italic text-sm">PR conversation data will appear here after re-run</p>'

    # Diff HTML
    v1_diff_html = diff_to_html(epic_data.get("v1_diff", ""))

    # Tables
    components_table = md_table_to_html(tables.get("Affected Components"))
    teams_table = md_table_to_html(tables.get("Impacted Teams"))

    # Screenshot helper
    def screenshot_slot(name, label):
        img_path = os.path.join(screenshots_base, f"{name}.png")
        if os.path.exists(img_path):
            rel = os.path.relpath(img_path, output_dir)
            return f'<img src="{escape(rel)}" alt="{escape(label)}" class="rounded-lg border border-slate-700 max-w-[50%]" />'
        return f"""
        <div class="border-2 border-dashed border-slate-600 rounded-lg p-8 text-center">
          <svg class="w-12 h-12 mx-auto mb-3 text-slate-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="1.5"
                  d="M4 16l4.586-4.586a2 2 0 012.828 0L16 16m-2-2l1.586-1.586a2 2 0 012.828 0L20 14m-6-6h.01M6 20h12a2 2 0 002-2V6a2 2 0 00-2-2H6a2 2 0 00-2 2v12a2 2 0 002 2z"/>
          </svg>
          <p class="text-slate-500 text-sm font-medium">{escape(label)}</p>
          <p class="text-slate-600 text-xs mt-1">screenshots/{escape(name)}.png</p>
        </div>"""

    pr_url = ""
    if epic_with_pr:
        pr_url = epic_with_pr.get("pr_url", "")
    epic_id_display = epic_with_pr["epic_id"] if epic_with_pr else "N/A"

    # Build per-run timeline from run-log.jsonl
    epic_summary_by_id = {ep["epic_id"]: ep for ep in epics}

    STATUS_LABELS = {
        "PRCreated": "PR Opened",
        "ReviewPending": "Code Generated",
        "Blocked": "Blocked",
        "Done": "Done",
        "InProgress": "In Progress",
        "Failed": "Failed",
    }

    def friendly_status(raw):
        return STATUS_LABELS.get(raw, raw)

    def build_run_timeline_blocks():
        """Build collapsible timeline blocks grouped by pipeline run."""
        if not run_log_entries:
            return '<p class="text-slate-500 italic text-sm">No pipeline runs recorded yet.</p>'

        blocks = ""
